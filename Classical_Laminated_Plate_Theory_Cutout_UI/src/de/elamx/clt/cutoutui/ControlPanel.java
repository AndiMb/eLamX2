    private boolean checkInput(CLT_Laminate laminat) {
        if (laminat.getCLTLayers().length == 0){
            DialogDisplayer.getDefault().notify(new NotifyDescriptor.Message(NbBundle.getMessage(ControlPanel.class, "Warning.nolaminate"), NotifyDescriptor.WARNING_MESSAGE));
            return false;
        }
        
        CutoutGeometry cg = data.getCutoutInput().getCutoutGeometry();
        double ratio = cg.getA()/cg.getB();
        if (ratio < 1.0/cg.getMaximumAspectRatio() || cg.getMaximumAspectRatio() < ratio){
            DialogDisplayer.getDefault().notify(new NotifyDescriptor.Message(NbBundle.getMessage(ControlPanel.class, "Warning.aspectratio"), NotifyDescriptor.WARNING_MESSAGE));
        }
        return true;
    }
