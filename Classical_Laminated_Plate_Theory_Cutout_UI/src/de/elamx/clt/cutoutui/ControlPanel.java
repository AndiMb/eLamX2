    private void calcButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcButtonActionPerformed
        calcButton.setEnabled(false);
        resultPanel.initListeners();
        final CLT_Laminate laminat = data.getLaminat().getLookup().lookup(CLT_Laminate.class);
        final ProgressHandle ph = ProgressHandle.createSystemHandle(NbBundle.getMessage(ControlPanel.class, "Task.cutoutcalc"), null);
        if (checkInput(laminat)){
            RequestProcessor.getDefault().post(new Runnable() {
                @Override
                public void run() {
                    ph.start();
                    final CutoutResult result = Cutout.calc(laminat, data.getCutoutInput());
                    EventQueue.invokeLater(new Runnable() {
                        @Override
                        public void run() {
                            data.setResult(result);
                            calcButton.setEnabled(true);
                            ph.finish();
                        }
                    });
                }
            });
        }else{
            calcButton.setEnabled(true);
        }
    }//GEN-LAST:event_calcButtonActionPerformed


