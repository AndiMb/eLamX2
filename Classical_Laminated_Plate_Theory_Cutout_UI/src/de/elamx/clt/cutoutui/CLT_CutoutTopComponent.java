    private class RawDataExport implements RawDataExportService {

        @Override
        public void export(FileWriter fw) {
            try {
                fw.write("");
                
                DecimalFormat df_coord = GlobalProperties.getDefault().getFormat(GlobalProperties.FORMAT_STRAIN);
                DecimalFormat df_phi   = GlobalProperties.getDefault().getFormat(GlobalProperties.FORMAT_ANGLE);
                DecimalFormat df_value = GlobalProperties.getDefault().getFormat(GlobalProperties.FORMAT_FORCE);
                
                String ls = System.getProperty("line.separator");
                
                // write cutoutgeometry
                fw.write("True scaled complex cutout geometry");
                fw.write(ls);
                fw.write("x" + "\t" + "y");
                fw.write(ls);
                double[][] geo = data.getCutoutInput().getCutoutGeometry().getScaledComplexGeometry();
                for (double[] geo1 : geo) {
                    fw.write(df_coord.format(geo1[0]) + "\t" + df_coord.format(geo1[1]));
                    fw.write(ls);
                }
                
                // write data over angle
                fw.write(ls);
                fw.write("--------------------------------------------------------------------------------");
                fw.write(ls);
                fw.write("Stress resultants over cutout angle");
                fw.write(ls);
                fw.write("alpha"//ResultPanel.ALPHA_KEY + " [°]"
                         + "\t" + ResultPanel.NXX_KEY
                         + "\t" + ResultPanel.NYY_KEY
                         + "\t" + ResultPanel.NXY_KEY
                         + "\t" + "nalpha"//ResultPanel.NALPHA_KEY
                         + "\t" + ResultPanel.MXX_KEY
                         + "\t" + ResultPanel.MYY_KEY
                         + "\t" + ResultPanel.MXY_KEY
                         + "\t" + "nalpha");//ResultPanel.MALPHA_KEY);
                fw.write(ls);
                
                CutoutResult res = data.getResult();
                for (int ii = 0; ii < res.getAlpha().length; ii++) {
                    fw.write(        df_phi.format(res.getAlpha()[ii])
                            + "\t" + df_value.format(res.getNxx()[ii])
                            + "\t" + df_value.format(res.getNyy()[ii])
                            + "\t" + df_value.format(res.getNxy()[ii])
                            + "\t" + df_value.format(res.getNAlpha()[ii])
                            + "\t" + df_value.format(res.getMxx()[ii])
                            + "\t" + df_value.format(res.getMyy()[ii])
                            + "\t" + df_value.format(res.getMxy()[ii])
                            + "\t" + df_value.format(res.getMAlpha()[ii]));
                    fw.write(ls);
                }
            } catch (IOException ex) {
                Exceptions.printStackTrace(ex);
            }
        }

        @Override
        public String getFileExtension() {
            return "txt";
        }
    }
    
    

    /**
     * Creates a line chart (based on an {@link XYDataset}) with default
     * settings.
     *
     * @param title  the chart title (<code>null</code> permitted).
     * @param xAxisLabel  a label for the X-axis (<code>null</code> permitted).
     * @param yAxisLabel  a label for the Y-axis (<code>null</code> permitted).
     * @param dataset  the dataset for the chart (<code>null</code> permitted).
     * @param orientation  the plot orientation (horizontal or vertical)
     *                     (<code>null</code> NOT permitted).
     * @param legend  a flag specifying whether or not a legend is required.
     * @param tooltips  configure chart to generate tool tips?
     * @param urls  configure chart to generate URLs?
     *
     * @return The chart.
     */
    public static JFreeChart createSquaredXYLineChart(String title, String xAxisLabel,
            String yAxisLabel, XYDataset dataset, PlotOrientation orientation,
            boolean legend, boolean tooltips, boolean urls) {

        Args.nullNotPermitted(orientation, "orientation");
        NumberAxis xAxis = new NumberAxis(xAxisLabel);
        xAxis.setAutoRangeIncludesZero(false);
        NumberAxis yAxis = new NumberAxis(yAxisLabel);       
        XYItemRenderer renderer = new XYLineAndShapeRenderer(true, false);
        XYPlotI plot = new XYPlotI(dataset, xAxis, yAxis, renderer);
        plot.setOrientation(orientation);
        
        if (tooltips) {renderer.setDefaultToolTipGenerator(new StandardXYToolTipGenerator());}
        if (urls)     {renderer.setURLGenerator(new StandardXYURLGenerator());}

        JFreeChart chart = new JFreeChart(title, JFreeChart.DEFAULT_TITLE_FONT,plot, legend);
        currentTheme.apply(chart);
        return chart;

    }
