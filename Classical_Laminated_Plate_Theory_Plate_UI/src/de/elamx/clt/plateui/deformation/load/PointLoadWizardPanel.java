package de.elamx.clt.plateui.deformation.load;

import javax.swing.event.ChangeListener;
import org.openide.WizardDescriptor;
import org.openide.util.HelpCtx;

public class PointLoadWizardPanel implements WizardDescriptor.Panel<WizardDescriptor> {

    /**
     * The visual component that displays this panel. If you need to access the
     * component from this class, just use getComponent().
     */
    private PointLoadVisualPanel component;

    // Get the visual component for the panel. In this template, the component
    // is kept separate. This can be more efficient: if the wizard is created
    // but never displayed, or not all panels are displayed, it is better to
    // create only those which really need to be visible.
    @Override
    public PointLoadVisualPanel getComponent() {
        if (component == null) {
            component = new PointLoadVisualPanel();
        }
        return component;
    }

    @Override
    public HelpCtx getHelp() {
        // Show no Help button for this panel:
        return HelpCtx.DEFAULT_HELP;
        // If you have context help:
        // return new HelpCtx("help.key.here");
    }

    @Override
    public boolean isValid() {
        // If it is always OK to press Next or Finish, then:
        return true;
        // If it depends on some condition (form filled out...) and
        // this condition changes (last form field filled in...) then
        // use ChangeSupport to implement add/removeChangeListener below.
        // WizardDescriptor.ERROR/WARNING/INFORMATION_MESSAGE will also be useful.
    }

    @Override
    public void addChangeListener(ChangeListener l) {
    }

    @Override
    public void removeChangeListener(ChangeListener l) {
    }

    @Override
    public void readSettings(WizardDescriptor wiz) {
        Object xPos = wiz.getProperty("xposition");
        if (xPos == null){
            xPos = 0.0;
        }
        Object yPos = wiz.getProperty("yposition");
        if (yPos == null){
            yPos = 0.0;
        }
        Object force = wiz.getProperty("force");
        if (force == null){
            force = 0.0;
        }
        
        component.setXPosition((Number)xPos);
        component.setYPosition((Number)yPos);
        component.setForce((Number)force);
    }

    @Override
    public void storeSettings(WizardDescriptor wiz) {
        wiz.putProperty("xposition", component.getXPosition());
        wiz.putProperty("yposition", component.getYPosition());
        wiz.putProperty("force", component.getForce());
    }

}
